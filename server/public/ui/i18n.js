/**
 * Translate a key. Supports {var} substitution.
 * t('profile.created', { name: 'My Profile' }) ‚Üí "Profil "My Profile" cr√©√© !"
 * Falls back to EN, then to the key itself.
 */
function t(key, vars) {
    let str = SF_LANGS[_sfLang]?.[key] ?? SF_LANGS.en?.[key] ?? key;
    if (vars) {
        Object.keys(vars).forEach(k => {
            str = str.replace(new RegExp('\\{' + k + '\\}', 'g'), vars[k]);
        });
    }
    return str;
}

